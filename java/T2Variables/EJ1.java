package T2Variables;

public class EJ1 {
    public static void main (String [] args){
        int x = 144;
        int y = 999;
        int suma = x+y;
        int resta = x-y;
        double divi = x/y;
        int multi = x*y;

        System.out.println("x = " + x);
        System.out.println("y = " + y);
        System.out.println("x + y = "+ suma);
        System.out.println("x - y = " + resta);
        System.out.println("x / y = " + divi);
        System.out.println("x * y = " + multi);

    }
}
